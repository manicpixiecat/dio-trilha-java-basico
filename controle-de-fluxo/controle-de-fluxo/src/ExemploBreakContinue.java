// class ExemploBreakContinue.java
public class ExemploBreakContinue {
	public static void main(String[] args) {
	
	for(int numero = 1; numero <=5; numero ++){
		if(numero==3)
			break;
		
		System.out.println(numero);
		
	}
 for(int numero = 1; numero <=10; numero ++){
		if(numero==5)
			continue;
		
		System.out.println(numero);
		
	}


    }
}
